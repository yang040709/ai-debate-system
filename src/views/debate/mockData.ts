export const mockData = async (lastItem: any) => {
  for (let i = 0; i < mockedData.content.length; i++) {
    await addContent(lastItem, i)
  }
}

function delay(ms = 2000) {
  return new Promise((resolve) => {
    setTimeout(resolve, ms)
  })
}

async function addContent(lastItem: any, j: number) {
  await delay(50)
  lastItem.content += mockedData.content.slice(j, j + 1)
  j++
}

